
public class BitwiseOperatorWithAnd {

	public static void main(String[] args) {
		int a = 10, b = 2;
	//	int c = a & b;
		System.out.println(a & b);
		System.out.println(a | b);
		System.out.println(a ^ b);
		System.out.println(~a);
		System.out.println(a<<1);
		System.out.println(a<<3);
		System.out.println(a>>1);
		System.out.println(a>>2);
		
	}

}
