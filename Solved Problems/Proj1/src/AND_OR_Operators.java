
public class AND_OR_Operators {

	public static void main(String[] args) {
		int a = 10, b = 10;
		System.out.println(a>10 && b>10);
		System.out.println(a>=10 && b>10);
		System.out.println(a>=10 && b>=10);
		System.out.println(a<10 && b<10);
		System.out.println(a<=10 && b<10);
		System.out.println(a<=10 && b<=10);
		System.out.println(a>10 || b>10);
		System.out.println(a>=10 || b>10);
		System.out.println(a>=10 || b>=10);
		System.out.println(a<10 || b<10);
		System.out.println(a<=10 || b<10);
		System.out.println(a<=10 || b<=10);

	}

}
